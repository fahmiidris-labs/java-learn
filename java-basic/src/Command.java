public class Command {
    public static void main(String[] args) {
        System.out.println(sum(10, 10));
    }

    /**
     * Method untuk menjumlahkan a dan b
     * @param a angka pertama
     * @param b angka kedua
     * @return hasil penjumlahan a dan b
     */
    static int sum(int a, int b) {
        // Menjumlahkan a + b
        return a + b;
    }
}
